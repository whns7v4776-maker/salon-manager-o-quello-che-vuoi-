export type TranslationKey = keyof typeof translations.it;

export const resolveStoredAppLanguage = (value?: string | null): AppLanguage =>
  SUPPORTED_LANGUAGES.includes((value ?? '') as AppLanguage) ? (value as AppLanguage) : 'it';

export const tApp = (
  language: AppLanguage,
  key: TranslationKey,
  params?: Record<string, string | number>
) => {
  const languageTable = translations[language] as Record<string, string>;
  const italianTable = translations.it as Record<string, string>;
  let value: string = languageTable[key] ?? italianTable[key];

  if (!params) return value;

  Object.entries(params).forEach(([paramKey, paramValue]) => {
    value = value.replaceAll(`{${paramKey}}`, String(paramValue));
  });

  return value;
};

